package ExemploProfessor1;

public class Aluno {
    public String nome;
    public int idade;
    public double altura;
    public int matricula;

    public Aluno(String nome, int idade, double altura, int matricula) {
        this.nome = nome;
        this.idade = idade;
        this.altura = altura;
        this.matricula = matricula;
    }
}
